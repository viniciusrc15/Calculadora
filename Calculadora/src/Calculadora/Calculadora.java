    private String calculaRAIZ(Double num1) {
        double rsp;
        String rspStr = "";
        //Calcula somente raizes inteira, poderia usar sqrt mas preferi fazer na mão
        for (int i = 0; i <= num1; i++) {
            if (num1 == (i * i)) {
                rsp = i;
                System.out.println(rsp);
                rspStr = String.valueOf(rsp);
            }
        }
        //Se não ouver raiz inteira informa e retorna " "
        if ("".equals(rspStr)) {
            JOptionPane.showMessageDialog(null, "Não ha raiz inteira");
        }
        return rspStr;

    }
