    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        if ("".equals(operacao)) {
            //Trabalha no fato de o usuario digitar igual sem nada antes
            System.out.println(num2);
            JOptionPane.showMessageDialog(null, "Você precisa primeiro selecionar uma operação");
        } else {
            if ("".equals(txfValor.getText())) {
                //Trabalha no fato de o usuario não digitar segundo valor
                txfValor.setText("0");
            }
            num2 = Double.parseDouble(txfValor.getText());
            switch (operacao) {
                case "+":
                    txtGuardaValor.setText(txtGuardaValor.getText() + txfValor.getText());
                    txfValor.setText(String.valueOf(num2 + num1));
                    //Apaga valores de operaçoes anteriores para não refazer operaçoes quando nao se insere outra
                    operacao = "";
                    num1 = 0.0;
                    num2 = 0.0;
                    break;
                case "/":
                    txtGuardaValor.setText(txtGuardaValor.getText() + txfValor.getText());
                    txfValor.setText(String.valueOf(num1 / num2));
                    //Apaga valores de operaçoes anteriores para não refazer operaçoes quando nao se insere outra
                    operacao = "";
                    num1 = 0.0;
                    num2 = 0.0;
                    break;
                case "*":
                    txtGuardaValor.setText(txtGuardaValor.getText() + txfValor.getText());
                    txfValor.setText(String.valueOf(num1 * num2));
                    //Apaga valores de operaçoes anteriores para não refazer operaçoes quando nao se insere outra
                    operacao = "";
                    num1 = 0.0;
                    num2 = 0.0;
                    break;
                case "-":
                    txtGuardaValor.setText(txtGuardaValor.getText() + txfValor.getText());
                    txfValor.setText(String.valueOf(num1 - num2));
                    //Apaga valores de operaçoes anteriores para não refazer operaçoes quando nao se insere outra
                    operacao = "";
                    num1 = 0.0;
                    num2 = 0.0;
                    break;
                /*case "RAIZ":
                txtGuardaValor.setText(txtGuardaValor.getText()+txfValor.getText());
                txfValor.setText(String.valueOf(calculaRAIZ(num1)));
                break;  */

            }
        }
    }//GEN-LAST:event_btnIgualActionPerformed

